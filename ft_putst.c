#include "ft_printf.h"

void	ft_putst(char *s, int *l)
{
	int	i;

	i = 0;
	if (!s)
		ft_putst("(null)", l);
	else
	{
		while (s[i])
		{
			ft_putch(s[i], l);
			i++;
		}
	}
}
