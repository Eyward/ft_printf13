#include "ft_printf.h"

void	ft_putindec(int n, int *l)
{
	long	nb;

	nb = n;
	if (nb < 0)
	{
		ft_putch('-', l);
		nb *= -1;
	}
	if (nb < 10)
		ft_putch(nb + 48, l);
	if (nb > 9)
	{
		ft_putindec(nb / 10, l);
		ft_putindec(nb % 10, l);
	}
}
